s = "hello"                   
s.upper()                     # "HELLO"
s.lower()                     # "hello"
s.strip()                     # elimina espacios alrededor
s.startswith("he")            # True si empieza con "he"
s.endswith("lo")              # True si termina con "lo"
s.split()                     # divide por espacios (por default)
"-".join(["a", "b"])          # "a-b"
s.replace("l", "x")           # reemplaza "l" por "x"
"e" in s                      # True si contiene "e"
